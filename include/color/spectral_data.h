Rgb chromaMatch__(float wl)
{
	Rgb r(0.0);
	if(wl < 360 || wl > 830) return r;
	int i = (int)((wl - 360) / 5);
	r.set(chroma_match_2_deg__[i][0], chroma_match_2_deg__[i][1], chroma_match_2_deg__[i][2]);
	return r;
}

