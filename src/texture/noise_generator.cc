const float *NoiseGenerator::hashPnt(int x, int y, int z)
{
	return hashpntf_ + 3 * hash_[(hash_[(hash_[z & 255] + y) & 255] + x) & 255];
}

void VoronoiNoiseGenerator::getFeatures(const Point3 &pt, float da[4], Point3 pa[4]) const
{
	int xx, yy, zz, xi, yi, zi;
	//float xd, yd, zd, d, *p;
	//float x=pt.x, y=pt.y, z=pt.z;
	float xd, yd, zd, d;
	float x = pt.x_, y = pt.y_, z = pt.z_;
	xi = (int)(floor(x));
	yi = (int)(floor(y));
	zi = (int)(floor(z));
	da[0] = da[1] = da[2] = da[3] = 1e10f;
	for(xx = xi - 1; xx <= xi + 1; xx++)
	{
		for(yy = yi - 1; yy <= yi + 1; yy++)
		{
			for(zz = zi - 1; zz <= zi + 1; zz++)
			{
				const float *p = hashPnt(xx, yy, zz);
				xd = x - (p[0] + xx);
				yd = y - (p[1] + yy);
				zd = z - (p[2] + zz);
				//d = (*distfunc)(xd, yd, zd, mk_exp);
				d = distfunc_2_(xd, yd, zd, mk_exp_);
				if(d < da[0])
				{
					da[3] = da[2];  da[2] = da[1];  da[1] = da[0];  da[0] = d;
					pa[3] = pa[2];  pa[2] = pa[1];  pa[1] = pa[0];  pa[0].set(p[0] + xx, p[1] + yy, p[2] + zz);
				}
				else if(d < da[1])
				{
					da[3] = da[2];  da[2] = da[1];  da[1] = d;
					pa[3] = pa[2];  pa[2] = pa[1];  pa[1].set(p[0] + xx, p[1] + yy, p[2] + zz);
				}
				else if(d < da[2])
				{
					da[3] = da[2];  da[2] = d;
					pa[3] = pa[2];  pa[2].set(p[0] + xx, p[1] + yy, p[2] + zz);
				}
				else if(d < da[3])
				{
					da[3] = d;
					pa[3].set(p[0] + xx, p[1] + yy, p[2] + zz);
				}
			}
		}
	}
}

float VoronoiNoiseGenerator::operator()(const Point3 &pt) const
{
	float da[4];
	Point3 pa[4];
	getFeatures(pt, da, pa);
	switch(v_type_)
	{
		case Vf2:
			return da[1];
		case Vf3:
			return da[2];
		case Vf4:
			return da[3];
		case Vf2F1:
			return da[1] - da[0];
		case VCrackle:
		{
			float t = 10.0 * (da[1] - da[0]);
			return (t > 1.0) ? 1.0 : t;
		}
		default:
		case Vf1:
			return da[0];
	}
}

// Cell noise
float CellNoiseGenerator::operator()(const Point3 &pt) const
{
	int xi = (int)(floor(pt.x_));
	int yi = (int)(floor(pt.y_));
	int zi = (int)(floor(pt.z_));
	unsigned int n = xi + yi * 1301 + zi * 314159;
	n ^= (n << 13);
	return ((float)(n * (n * n * 15731 + 789221) + 1376312589) / 4294967296.0);
}

